int		ft_nbchiffres(int n)
{
	int			nb;

	nb = 1;
	if (n == -2147483648)
		return (12);
	if (n == 2147483647)
		return (11);
	if (n < 0)
		n = -n;
	while (n > 0)
	{
		n /= 10;
		nb++;
	}
	return (nb);
}
